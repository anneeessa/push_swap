#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (s[i] == (char) c)
			return (i + (char *)(s));
		i++;
	}
	if (s[i] == (char) c)
		return ((char *)s + i);
	return (0);
}
